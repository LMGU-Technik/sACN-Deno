describe('multicastGroup', () => {
  describe('test valid universes', () => {
    it('should equal', () => assert.equal(multicastGroup(1), '239.255.0.1'));
    it('should equal', () => assert.equal(multicastGroup(2), '239.255.0.2'));
    it('should equal', () => assert.equal(multicastGroup(255), '239.255.0.255'));
    it('should equal', () => assert.equal(multicastGroup(256), '239.255.1.0'));
    it('should equal', () => assert.equal(multicastGroup(63999), '239.255.249.255'));
    it('should equal', () => assert.equal(multicastGroup(64214), '239.255.250.214'));
  });
  describe('test invalid universes', () => {
    it('should throw an error', () => assert.throws(() => multicastGroup(0)));
    it('should throw an error', () => assert.throws(() => multicastGroup(64000)));
    it('should throw an error', () => assert.throws(() => multicastGroup(NaN)));
    it('should throw an error', () => assert.throws(() => multicastGroup(Infinity)));
  });
});
